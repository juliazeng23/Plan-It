import Foundation
import UIKit

extension UIColor {
    static let PI = PlanItColors()

    struct PlanItColors {
        let black = UIColor.black
        let offWhite = UIColor(red: 249/255, green: 249/255, blue: 249/255, alpha: 1)
        let silver = UIColor(red: 191/255, green: 191/255, blue: 191/255, alpha: 1)
        let white = UIColor.white
    }

}

